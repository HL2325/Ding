const showMsg = function(Icon,msg) {
	dd.showToast({
		type: Icon,
		content: msg,
		duration: 1300
	});
};

const showLoading = function() {
	dd.showLoading({
		content: '正在加载...',
		delay: 0
	})
}

module.exports = {
	baseUrl: baseUrl,
	showMsg: showMsg,
	showLoading: showLoading
}